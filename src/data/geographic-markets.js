// Helper functions
export function getMarketBySlug(slug) {
  return geographicMarkets.find(market => market.slug === slug);
}

export function getMarketById(id) {
  return geographicMarkets.find(market => market.id === id);
}

export function getAllMarketSlugs() {
  return geographicMarkets.map(market => market.slug);
}

export function getMarketsByType(type) {
  return geographicMarkets.filter(market => market.type === type);
}

export function getCountries() {
  return geographicMarkets.filter(market => market.type === 'country');
}

export function getProvinces() {
  return geographicMarkets.filter(market => market.type === 'province');
}

export function getStates() {
  return geographicMarkets.filter(market => market.type === 'state');
}
